function studentGrade(grade){
    

switch (grade) {
    case 'A':
        console.log("Excellent Mark");
        break;
    case 'B':
        console.log("Good Mark");
        break;   
    case 'C':
        console.log("Average Mark");
        break;            
    case 'D':
        console.log("Below Average Mark");
         break;   
    default:
        console.log("Failed");
       
}
}
studentGrade('A')
